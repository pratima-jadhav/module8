package module8;

import static java.lang.Math.*;

public class StaticImport {

	public static void main(String[] args) {
		System.out.println(pow(21, 2));
		System.out.println(pow(1, 4));
		System.out.println(pow(5, 6));

		System.out.println(random());
		System.out.println(round(55.7));
		System.out.println(round(34.2));
		System.out.println(round(12.9));
		System.out.println(max(34, 67));
		System.out.println(ceil(56));
	}

}
